package Modelo;

public class Encargado {
    private String cod_encargado;
    private String nom_encargado;
    private String contraseña;


    public String getCod_encargado() {
        return cod_encargado;
    }

    public void setCod_encargado(String cod_encargado) {
        this.cod_encargado = cod_encargado;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    public String getNom_encargado() {
        return nom_encargado;
    }

    public void setNom_encargado(String nombre_encargado) {
        this.nom_encargado = nombre_encargado;
    }
}
