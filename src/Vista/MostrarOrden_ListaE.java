    private void mostrarElementos() {
    if(lista.getInicio()== null){
            txtA.append("Lista VacÃ­a...\n");
        }
        else{
            Nodo elemento= lista.getInicio();
            while(elemento != null){
                txtA.append(elemento.getEditorial().getCod_editorial()+ "-->");
                elemento= elemento.getSig();
            }
            txtA.append("\n");
        }
    }
    
